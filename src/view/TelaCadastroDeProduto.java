    public void atualisaTabela() {
        DefaultTableModel model = (DefaultTableModel) tblProduto.getModel();
        model.setNumRows(0);
        ProdutoServiceImpl service = new ProdutoServiceImpl();

        service.getProdutos().forEach((p) -> {
            model.addRow(new Object[]{p.getId(),
                p.getDescricao(),
                p.getQntd(),
                p.getPreco()});
        });
    }

    public void limparCampos() {
        for (int i = 0; i < painelCadastroProduto.getComponentCount(); i++) {
            if (painelCadastroProduto.getComponent(i) instanceof JTextField) {
                ((JTextField) painelCadastroProduto.getComponent(i)).setText("");
            }

        }

    }

